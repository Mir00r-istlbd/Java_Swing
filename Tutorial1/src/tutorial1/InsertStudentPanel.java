    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con = null;

        String Name = null;
        String Roll = null;
        String clas = null;

        int NameLen = 0;
        int RollLen = 0;
        int clasLen = 0;

        try {
            DatabaseConnection databaseConnection = new DatabaseConnection();
            try {
                con = databaseConnection.getConnection();
            } catch (Exception ex) {
                Logger.getLogger(InsertStudentPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

            Name = name.getText();
            Roll = roll.getText();
            clas = className.getText();

            NameLen = Name.length();
            RollLen = Roll.length();
            clasLen = clas.length();

            if ( NameLen != 0 && RollLen != 0 && clasLen != 0 ) {

                String sql = "INSERT INTO student (name, roll, class) VALUES(?,?,?)";
                PreparedStatement PST = con.prepareStatement(sql);

                PST.setString(1, Name);
                PST.setString(2, Roll);
                PST.setString(3, clas);

                PST.executeUpdate();

                PST.close();
                con.close();

                JFrame frame = new JFrame();

                JOptionPane.showMessageDialog(frame,
                    "DATA INSERTED SUCCESSFULLY");

                name.setText("");
                roll.setText("");
                className.setText("");

            } else {
                JFrame frame = new JFrame();

                JOptionPane.showMessageDialog(frame,
                    "Please fill up all the fields");
            }

        } catch (Exception e) {

            System.out.println("DATA NOT INSERTED");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

