    private void executeUserSearchReq() {

        
        
        // initialize the object for search
        UserManagerBean pmb = new UserManagerBean();
        BioUser person = new BioUser();

            //Bangla Name
            person.setUsername(inp_search_bar.getText());

            GetUserSummaryResponse resp = new GetUserSummaryResponse();

            resp = pmb.getAdvancedSearchUserInfo(0L, 1000L, person);

        if (resp != null && resp.isOperationStatus() && resp.getUserList()!= null && resp.getUserList().size() > 0) {

            lbl_search_total_records.setText(Defs.SEARCH_TOTAL_RECORDS_TITLE + resp.getTotalResult());

            if (tabledata == null) {
                tabledata = new ArrayList<BioUser>();
            }
            tabledata.clear();
            tabledata.addAll(resp.getUserList());
        } else {

            lbl_search_total_records.setText(Defs.SEARCH_TOTAL_RECORDS_TITLE + "0");
            if (tabledata != null) {
                tabledata.clear();
            }
        }

        isPaginationInitialized = false;
        generateBackupHistoryList(1);

    }

