    public static void viewDetails(String details_id) {
        Lookup.userData=new BioUser();

        
        UserManagerBean umb=new UserManagerBean();
        
        GetUserSummaryResponse usr=new GetUserSummaryResponse();
        
        usr=umb.getSingleUserInfo(details_id);
        UserCreateForm ucf=new UserCreateForm();
        if(usr!=null && usr.isOperationStatus())
        {
            
            BioUser bo=usr.getSingleUser();
            
            ucf.userNameField.setText(bo.getUsername());
            ucf.passwordField.setText(bo.getPassword());
            ucf.fullnameField.setText(bo.getFullName());
            ucf.designationField.setText(bo.getDesignation());
            ucf.phoneNoField.setText(bo.getPhoneNo());
            ucf.emailField.setText(bo.getEmail());
            ucf.nidField.setText(bo.getNationalIdNo());
            
            Lookup.userData.setUsername(bo.getUsername());
            Lookup.userData.setPassword(bo.getPassword());
            Lookup.userData.setFullName(bo.getFullName());
            Lookup.userData.setDesignation(bo.getDesignation());
            Lookup.userData.setPhoneNo(bo.getPhoneNo());
            Lookup.userData.setEmail(bo.getEmail());
            Lookup.userData.setId(bo.getId());
            Lookup.userData.setNationalIdNo(bo.getNationalIdNo());
            Lookup.userData.setRoleId(bo.getRoleId());
            
            ucf.prepareRoleDropDown();
            ucf.setVisible(true);
            
            
        }
        else
        {
            ucf.dispose();
            System.out.println("Exception occurred, dont open form, show error message in box");
            Utils.showErrorMsg("An Error occured.User data not found.");
        
        }
                
        
        
        


    }

